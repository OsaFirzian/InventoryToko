    private void PasswordUserFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_PasswordUserFocusGained
        if (String.valueOf(PasswordUser.getPassword()).equals("     Password")) {
            PasswordUser.setText("");
            PasswordUser.setEchoChar('*'); // Tampilkan bintang saat mulai mengetik
            PasswordUser.setForeground(new java.awt.Color(204, 204, 204));
            PasswordUser.setFont(new java.awt.Font("Segoe UI", 0, 16));
        }
    }//GEN-LAST:event_PasswordUserFocusGained

    private void PasswordUserFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_PasswordUserFocusLost
        if (PasswordUser.getPassword().length == 0) {
            PasswordUser.setEchoChar((char) 0); // Hapus bintang
            PasswordUser.setText("     Password");
            PasswordUser.setForeground(new java.awt.Color(204, 204, 204));
            PasswordUser.setFont(new java.awt.Font("Segoe UI", 0, 16));
        }
    }//GEN-LAST:event_PasswordUserFocusLost

