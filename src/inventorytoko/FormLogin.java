    private void ButtonLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ButtonLoginMouseClicked
        String username = TeksUser.getText();
        String password = String.valueOf(PasswordUser.getPassword());

        try {
        koneksi con = new koneksi(); // objek dari class koneksi
        Connection conn = con.connect();

        String sql = "SELECT * FROM inventorytoko.user WHERE Nama = ? AND Password = ?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, username);
        pst.setString(2, password);

        ResultSet rs = pst.executeQuery();

//        if (rs.next()) {JOptionPane.showMessageDialog(this, "Login berhasil sebagai " + role);
//            String role = rs.getString("role");
//            JOptionPane.showMessageDialog(this, "Login berhasil sebagai " + role);
//
//            this.setVisible(false); // sembunyikan login form
//
//            if (role.equals("admin")) {
//                new FormDashboard(username).setVisible(true);
//            } else if (role.equals("user")) {
//                new FormStokbarang(username).setVisible(true);
//            }
//        } else {
//            JOptionPane.showMessageDialog(this, "Username atau password salah!");
//        }
        
        if (rs.next()) {
            String role = rs.getString("role");
            JOptionPane.showMessageDialog(this, "Login berhasil sebagai " + role);
            this.setVisible(false); // sembunyikan login form

            if (role.equals("Admin")) {
                new FormDashboard(username).setVisible(true);
            } else if (role.equals("Staff")) {
                new FormStokbarang(username).setVisible(true);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Username atau password salah!");
        }

        rs.close();
        pst.close();
        conn.close();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal login: " + e.getMessage());
    }
    }//GEN-LAST:event_ButtonLoginMouseClicked

